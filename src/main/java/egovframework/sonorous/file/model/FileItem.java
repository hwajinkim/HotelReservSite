package egovframework.sonorous.file.model;

public class FileItem {

	private int roomImageId;
	private int roomId;
	private String roomImageName;
	private String roomImagePath;
	
	public int getRoomImageId() {
		return roomImageId;
	}
	public void setRoomImageId(int roomImageId) {
		this.roomImageId = roomImageId;
	}
	public int getRoomId() {
		return roomId;
	}
	public void setRoomId(int roomId) {
		this.roomId = roomId;
	}
	public String getRoomImageName() {
		return roomImageName;
	}
	public void setRoomImageName(String roomImageName) {
		this.roomImageName = roomImageName;
	}
	public String getRoomImagePath() {
		return roomImagePath;
	}
	public void setRoomImagePath(String roomImagePath) {
		this.roomImagePath = roomImagePath;
	}
}
